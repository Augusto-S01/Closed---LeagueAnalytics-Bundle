package com.LeagueAnalytics.dto.leagueDTO;

public class ObjectivesDTO {
    private BaronDTO baron;
    private ChampionDTO champion;
    private DragonDTO dragon;
    private InhibitorDTO inhibitor;
    private RiftHeraldDTO riftHerald;
    private TowerDTO tower;



    public BaronDTO getBaron() {
		return baron;
	}

	public void setBaron(BaronDTO baron) {
		this.baron = baron;
	}

	public ChampionDTO getChampion() {
		return champion;
	}

	public void setChampion(ChampionDTO champion) {
		this.champion = champion;
	}

	public DragonDTO getDragon() {
		return dragon;
	}

	public void setDragon(DragonDTO dragon) {
		this.dragon = dragon;
	}

	public InhibitorDTO getInhibitor() {
		return inhibitor;
	}

	public void setInhibitor(InhibitorDTO inhibitor) {
		this.inhibitor = inhibitor;
	}

	public RiftHeraldDTO getRiftHerald() {
		return riftHerald;
	}

	public void setRiftHerald(RiftHeraldDTO riftHerald) {
		this.riftHerald = riftHerald;
	}

	public TowerDTO getTower() {
		return tower;
	}

	public void setTower(TowerDTO tower) {
		this.tower = tower;
	}

	
}






